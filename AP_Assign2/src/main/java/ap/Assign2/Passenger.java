package ap.Assign2;

public class Passenger {

	String name;
	String gender;
	int age;
	String address;
	String passNo;
	
	Passenger(){
		name=null;
		gender=null;
		age=0;
		address=null;
		passNo=null;

	}
	
	
	Passenger(String n, String g, int ag, String a, String pa){
		name=n;
		gender=g;
		age=ag;
		address=a;
		passNo=pa;
	}
	
	
	
	
	
}
